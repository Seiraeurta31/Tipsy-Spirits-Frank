import Head from "next/head";
import Link from "next/link";
import Image from "next/image";
import { useRouter } from "next/router";
import { withIronSessionSsr } from "iron-session/next";
import sessionOptions from "../config/session";
import styles from "../styles/Home.module.css";
import Header from "../components/header";
import Footer from "../components/footer";
import useLogout from "../hooks/useLogout";
import { getRandomDrink } from "../util/drinks";


export const getServerSideProps = withIronSessionSsr(
  async function getServerSideProps({ req }) {
    const user = req.session.user;
    const props = {};
    if (user) {
      props.user = req.session.user;
      props.isLoggedIn = true;
    } else {
      props.isLoggedIn = false;
    }


  //TODO: Get random drink  
  const randomDrink = await getRandomDrink()
  if(randomDrink){
    props.randomDrink = randomDrink
  } 



    return { props };
  },
  sessionOptions
);

export default function Home(props) {
  const router = useRouter();
  const randomDrink = props.randomDrink //an array of drinks

  return (
    <div >
      <Head>
        <title>Tipsy Spirits</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Header isLoggedIn={props.isLoggedIn} username={props?.user?.username} />

      <main >
        <h1 className={styles.sitename}>
          Welcome to Tipsy Spirits
        </h1>
   

        <div >
          {props.isLoggedIn ? (
            <>
              <div>
              {randomDrink
                //If random drink exists
                ? <section>
                  <div> 
                    {randomDrink.map((drink, i) => (
                      <RandomDrink 
                        key={i}
                        id={drink.cocktailDbId} 
                        name={drink.name} 
                        image={drink.image}>
                      </RandomDrink>
                    ))}
                  </div>
                </section>
                  //If no drinks found, display message
                : <p>No Drinks Found!</p>
              }
              </div>

              <Link href="/favorites" >
                <h2>My Favorites</h2>
              </Link>
  
            </>
          ) : (
            <>
              <Link href="/login" >
                <h2>Login </h2>
              </Link>

              <Link href="/signup" >
                <h2>Sign Up </h2>
              </Link>
            </>
          )}
        </div>
      </main>

      <Footer/>
    </div>
  );
}

function RandomDrink({id, name, image}) {
  const noImage = "/No_image_available.svg.png"
  return (
      <Link href={'/drink/' + id}>
        <h1>{name}</h1>
        <Image src={image ? image : noImage} width="300" height="300" alt="picture"/>
        <span></span>
      </Link>
    
  )
}